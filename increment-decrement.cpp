#include <iostream>

using namespace std;

int main()
{
    int x = 5;

    x++;

    cout << x << endl;

    x = 5;

    ++x;

    cout << x << endl;

    x = 5;

    cout << ++x << endl;

    x = 5;

    cout << x++ << endl;

    x = 5;

    cout << --x << endl;

    x = 5;

    cout << x-- << endl;

    return 0;
}
